#while loop არის ციკლი (loop) რომელიც კოდს ამეორებს მანამ სანამ მოცემული პირობა True არის.
# თუ პირობა True არის -> კოდი შესრულდება
# თუ პირობა False გახდა -> ციკლი გაჩერდება

# მაგალითი:

num = 1

while num <= 5:
    print(num)
    num += 1


#ამ კოდში ტერმინალშ გამოვა რიცხვები 1იდან 5ის ჩათვლით რადგან ჩვენ მივეცით პირობა რომ რიცხვი გაეზარდა ერთით

# while გამოიყენება:
